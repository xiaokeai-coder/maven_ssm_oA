package com.tledu.tt.model;

/**
 * 权限
 * 
 * @author 天亮教育
 * @Date 2020年4月24日
 */
public class Role {
	private int id;
	private String name;
	private String desc;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

}
